import polars as pl
import kagglehub
import os
import time

def run_enterprise_pipeline():
    # 1. DATA INGESTION
    try:
        path = kagglehub.dataset_download("sobhanmoosavi/us-accidents")
        csv_file = [f for f in os.listdir(path) if f.endswith('.csv')][0]
        data_source = os.path.join(path, csv_file)
    except Exception as e:
        print(f"File Access Error: {e}")
        return

    # 2. DEFINING REUSABLE EXPRESSIONS (Modular Logic)
    duration_mins = (
        (pl.col("End_Time").str.to_datetime(strict=False) -
         pl.col("Start_Time").str.to_datetime(strict=False))
        .dt.total_minutes()
    )

    # Demonstrating: Projection Pushdown, Predicate Pushdown, and Type Casting
    main_plan = (
        pl.scan_csv(data_source, infer_schema_length=10000)
        .select([
            "ID", "Severity", "Start_Time", "End_Time", "State",
            "Temperature(F)", "City", "Visibility(mi)"
        ])
        .filter(
            (pl.col("State").is_in(["CA", "TX", "FL", "NY"])) &
            (pl.col("Severity").is_not_null())
        )
        .with_columns([
            duration_mins.alias("Duration_Mins"),
            pl.col("Temperature(F)").fill_null(strategy="mean")
        ])
        .filter(pl.col("Duration_Mins") > 0)
    )

    analytics_plan = main_plan.with_columns([
        pl.col("Temperature(F)").mean().over("State").alias("State_Avg_Temp"),
        # Rank cities by longest average accident duration
        pl.col("Duration_Mins")
        .rank(method="dense", descending=True)
        .over("State")
        .alias("Severity_Rank_In_State")
    ])

    state_mapping = pl.LazyFrame({
        "State": ["CA", "TX", "FL", "NY"],
        "State_Full_Name": ["California", "Texas", "Florida", "New York"]
    })

    final_plan = analytics_plan.join(state_mapping, on="State", how="left")

    print("LOGICAL AND PHYSICAL QUERY PLAN")
    print("-" * 30)
    print(final_plan.explain())
    print("-" * 30)



    # 7. STREAMING EXECUTION (Out-of-Core Processing)
    # This allows processing datasets larger than physical RAM
    start_time = time.time()

    # execute the plan
    df_result = final_plan.collect(engine="streaming")

    execution_time = time.time() - start_time

    # 8. DATA SINK (Parquet Partitioning)
    # Parquet is the standard for Big Data storage (OLAP)
    output_dir = "optimized_accident_data"
    if not os.path.exists(output_dir):
        os.makedirs(output_dir)

    df_result.write_parquet(
        f"{output_dir}/processed_accidents.parquet",
        compression="zstd"
    )

    print(f"Process Telemetry:")
    print(f"Execution Time: {execution_time:.4f} seconds")
    print(f"Record Count: {df_result.height}")
    print(f"Memory Usage: {df_result.estimated_size('mb'):.2f} MB")
    print(df_result.head(10))

if __name__ == "__main__":
    run_enterprise_pipeline()

