import polars as pl
import numpy as np

# DIMENSION 1: Products
products_dim = pl.LazyFrame({
    "product_id": [1, 2, 3],
    "category": ["Electronics", "Furniture", "Electronics"],
    "brand": ["Brand_A", "Brand_B", "Brand_A"]
})

# DIMENSION 2: Stores
stores_dim = pl.LazyFrame({
    "store_id": ["S_01", "S_02"],
    "city": ["New York", "London"],
    "region": ["North", "Europe"]
})

# FACT: Sales (1 million records)
sales_fact = pl.DataFrame({
    "order_id": np.arange(1_000_000),
    "product_id": np.random.randint(1, 4, 1_000_000),
    "store_id": np.random.choice(["S_01", "S_02"], 1_000_000),
    "amount": np.random.uniform(10, 1000, 1_000_000)
}).lazy()

# The Model Pipeline
model_query = (
    sales_fact
    # Step 1: Enrich Facts with Dimensions
    .join(products_dim, on="product_id", how="left")
    .join(stores_dim, on="store_id", how="left")

    # Step 2: Filter the Model (Predicate Pushdown)
    .filter(pl.col("category") == "Electronics")

    # Step 3: Complex Analytics (Window Functions)
    .with_columns([
        # Compare each sale to the average of its specific Brand
        pl.col("amount").mean().over("brand").alias("brand_avg_sale"),
        # Total sales per Region
        pl.col("amount").sum().over("region").alias("regional_total")
    ])

    # Step 4: Final Selection (Projection Pushdown)
    .select(["order_id", "city", "brand", "amount", "brand_avg_sale"])
)

model_query.explain()

final_output = model_query.collect(engine="streaming")

